package com.chinadovey.parking.webapps.ex.manage;

public class ExCarChargeRecordList{
	private String carNo;
	private float price;
	private Integer parkingSpan;
	private String time;
	private String parkName;
	private Integer parkId;
	public String getCarNo() {
		return carNo;
	}
	public void setCarNo(String carNo) {
		this.carNo = carNo;
	}
	public float getPrice() {
		return price;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public Integer getParkingSpan() {
		return parkingSpan;
	}
	public void setParkingSpan(Integer parkingSpan) {
		this.parkingSpan = parkingSpan;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getParkName() {
		return parkName;
	}
	public void setParkName(String parkName) {
		this.parkName = parkName;
	}
	public Integer getParkId() {
		return parkId;
	}
	public void setParkId(Integer parkId) {
		this.parkId = parkId;
	}
	
		

	 
}
